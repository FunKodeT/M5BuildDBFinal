-- Create the 'Enrolled Students' Table
CREATE TABLE
	students(
		s_id SERIAL PRIMARY KEY,
		fname VARCHAR(100) NOT NULL,
		mname VARCHAR(100),
		lname VARCHAR(100) NOT NULL,
		email VARCHAR(100) NOT NULL UNIQUE,
		phone VARCHAR(25) NOT NULL,
		address VARCHAR(100) NOT NULL,
		ice_contact INTEGER NOT NULL,
		ice_phone VARCHAR(25) NOT NULL,
		ice_fname VARCHAR(100) NOT NULL,
		ice_lname VARCHAR(100) NOT NULL
	);
	
-- Create the 'Available Courses' Table
CREATE TABLE
	courses(
		c_id SERIAL PRIMARY KEY,
		subject VARCHAR(100) NOT NULL,
		prof_fname VARCHAR(100) NOT NULL,
		prof_lname VARCHAR(100) NOT NULL,
		max_cap INTEGER NOT NULL,
		cur_cap INTEGER NOT NULL,
		prof_phone VARCHAR(25) NOT NULL,
		campus_phone VARCHAR(25) NOT NULL,
		prof_email VARCHAR(100) NOT NULL,
		campus_email VARCHAR(100) NOT NULL
	);

